            <h3 class="pt-3">Domaine d’expertise et bref historique</h3>
            <p>
              Initialement, la mission principale de l’ENSMR (ex ENIM) a été de former des ingénieurs pour le secteur minier et l’industrie minérale.
			  Ainsi entre 1975 et 1983, 447 ingénieurs ont été formés dans les domaines de la Géologie, des Mines et traitement des Minerais et 61
			  ingénieurs dans le domaine de la métallurgie. Cette dernière spécialité a été créée en 1977 pour accompagner les efforts du pays dans
			  le domaine de la sidérurgie et du développement de l’Industrie métallurgique lancée à cette époque. La majeure partie des ingénieurs
			  formés entre 1972 et 1983 ont effectivement fait carrière dans les secteurs ayant trait à la mine, l’industrie minérale et métallurgique
			  (OCP, ONA, ONCF, ONAREP, BRPM, Ministère de l’Energie et des Mines…). Le cursus de formation de ces ingénieurs a été étalé sur 4 ans
			  répartis en deux cycles.<br>
			  En 1983, l’ENSMR (ex ENIM) a vu la création de deux nouveaux départements, à savoir l’Electromécanique et le
			  Génie Chimique Energétique, afin de répondre aux besoins nationaux dans ces domaines. En effet au début des années 80, l’industrie
			  nationale, de même que le secteur minier, a connu un développement et modernisation dans les outils de production nécessitant l’intervention
			  d’un nouveau profil d’ingénieur : Electromécanique. De même que l’industrie chimique et de transformation a connu un élan remarquable
			  (avec notamment le lancement des projets Maroc Phosphore II, III et IV), ce qui a justifié et incité à la création du département Génie
			  Chimique Energétique. A la même période, le département Métallurgie a été transformé en Génie des matériaux, le département Géologie en
			  Sciences de la Terre et les départements Exploitation des Mines et Valorisation des minerais ont été fusionnés en un seul département :
			  Département Mines, assurant une formation polyvalente en Génie Minier. Le cursus de formation a été étalé sur six ans après le baccalauréat.<br>
			  En 1990, une réforme des études a eu lieu pour s’adapter à la suppression des cycles des années préparatoires dans les écoles d’ingénieurs et
			  la création des centres de classes préparatoires aux grandes écoles (CPGE) à l’échelle nationale. Ceci a conduit l’ENSMR (ex ENIM) à mené une
			  profonde réflexion sur son cursus de formation. Le régime transitoire proposé et approuvé par les conseils de perfectionnement et d’administration<br>
			  En 1996 a été initiée une autre réforme des études visant à créer de nouvelles spécialités et à former des ingénieurs polyvalents et
			  pluridisciplinaires pouvant répondre aux nouvelles exigences du marché du travail.<br>
